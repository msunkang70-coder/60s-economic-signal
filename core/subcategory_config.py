def get_subcategory_list(industry_key: str) -> list[str]:
    """해당 산업의 서브카테고리 키 목록 반환. 항상 '전체' 포함.

    미등록 산업이면 ["전체"]만 반환.
    """
    rules = SUBCATEGORY_RULES.get(industry_key, {})
    if not rules:
        return ["전체"]
    return list(rules.keys())


def get_subcategory_rules(industry_key: str, subcategory: str) -> dict:
    """서브카테고리의 include/block/boost 규칙 반환.

    '전체' 또는 미등록 키 → 빈 규칙 (기존 로직 유지).
    """
    rules = SUBCATEGORY_RULES.get(industry_key, {})
    sub = rules.get(subcategory, {"label": "전체", "include": [], "block": [], "boost": []})
    return sub


def get_subcategory_label(industry_key: str, subcategory: str) -> str:
    """서브카테고리의 표시용 label 반환."""
    rules = SUBCATEGORY_RULES.get(industry_key, {})
    sub = rules.get(subcategory, {})
    return sub.get("label", subcategory)
